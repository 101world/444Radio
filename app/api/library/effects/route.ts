/**
 * Library Effects API - Fetch user's generated effects
 */

import { NextResponse } from 'next/server';
import { auth } from '@clerk/nextjs/server';
import { corsResponse, handleOptions } from '@/lib/cors';

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL!;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY!;

export async function OPTIONS() {
  return handleOptions();
}

export async function GET() {
  try {
    const { userId } = await auth();
    if (!userId) {
      return corsResponse(NextResponse.json({ error: 'Unauthorized' }, { status: 401 }));
    }

    // Fetch effects generated by this user from combined_media table
    // Use service role key to bypass RLS
    const res = await fetch(
      `${supabaseUrl}/rest/v1/combined_media?user_id=eq.${userId}&genre=eq.effects&order=created_at.desc&select=id,title,audio_url,image_url,created_at,plays,metadata,prompt`,
      {
        headers: {
          apikey: supabaseKey,
          Authorization: `Bearer ${supabaseKey}`,
        },
      }
    );

    if (!res.ok) {
      console.error('Supabase error fetching effects:', res.status);
      return corsResponse(NextResponse.json({ error: 'Failed to fetch effects' }, { status: 500 }));
    }

    const data = await res.json();

    return corsResponse(NextResponse.json({
      success: true,
      effects: data || [],
    }));
  } catch (error) {
    console.error('Error in /api/library/effects:', error);
    return corsResponse(NextResponse.json({ error: 'Internal server error' }, { status: 500 }));
  }
}
